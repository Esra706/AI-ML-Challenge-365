
# Basic range - 0 to 4
print("Basic range(5):")
for i in range(5):
    print(i)

# range with start and stop
print("\nrange(1, 6):")
for i in range(1, 6):
    print(i)

# range with step
print("\nrange(0, 10, 2):")
for i in range(0, 10, 2):
    print(i)

# range with negative step (counting down)
print("\nrange(10, 0, -1):")
for i in range(10, 0, -1):
    print(i)